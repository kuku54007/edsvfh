show_named_bg_status() {
  local pid_file="$1"
  local name="$(basename "${pid_file}" .pid)"
  local pid=""
  if [[ -f "${pid_file}" ]]; then
    pid="$(<"${pid_file}")"
  fi
  local meta_file="${BG_STATE_ROOT}/${name}.meta"
  local log_path=""
  if [[ -f "${meta_file}" ]]; then
    log_path="$(grep -E '^log=' "${meta_file}" | head -n1 | cut -d= -f2-)"
  fi
  echo "### ${name}"
  if [[ -n "${pid}" ]] && ps -p "${pid}" > /dev/null 2>&1; then
    ps -p "${pid}" -o pid,etime,cmd
  else
    echo "status=not_running pid=${pid:-unknown}"
  fi
  if [[ -n "${log_path}" ]]; then
    echo "log=${log_path}"
  fi
}

