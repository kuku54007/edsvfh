  shift
  log_step "START ${label}"
  local start_ts=$(date +%s)
  "$@"
  local rc=$?
  local end_ts=$(date +%s)
  local elapsed=$(( end_ts - start_ts ))
  log_step "END   ${label} | elapsed $(format_seconds ${elapsed}) | exit=${rc}"
  return ${rc}
}

_format_cmd_quoted() {
  local out=""
  local token
  for token in "$@"; do
    out+="$(printf '%q' "${token}") "
  done
  printf '%s' "${out% }"
}

_spawn_detached() {
  local log_path="$1"
  shift
  if command -v setsid >/dev/null 2>&1; then
    setsid nohup "$@" >> "${log_path}" 2>&1 < /dev/null &
  else
    nohup "$@" >> "${log_path}" 2>&1 < /dev/null &
  fi
  echo "$!"
}

launch_bg() {
  local log_path="$1"
  shift
  mkdir -p "$(dirname "${log_path}")"
  _spawn_detached "${log_path}" "$@"
}

launch_bg_named() {
  local name="$1"
  local log_path="$2"
  shift 2
  mkdir -p "$(dirname "${log_path}")" "${BG_STATE_ROOT}"
  local pid_file="${BG_STATE_ROOT}/${name}.pid"
  local meta_file="${BG_STATE_ROOT}/${name}.meta"
  local cmd_string
  cmd_string="$(_format_cmd_quoted "$@")"
  {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] START background ${name}"
    echo "PROJECT_DIR=${PROJECT_DIR}"
    echo "WORKSPACE_ROOT=${WORKSPACE_ROOT}"
    echo "CMD=${cmd_string}"
  } >> "${log_path}"
  local pid
  pid="$(_spawn_detached "${log_path}" "$@")"
  printf '%s\n' "${pid}" > "${pid_file}"
  {
    echo "name=${name}"
    echo "pid=${pid}"
    echo "log=${log_path}"
    echo "started_at=$(date -Is)"
    echo "cmd=${cmd_string}"
  } > "${meta_file}"
  echo "${pid}"
}

