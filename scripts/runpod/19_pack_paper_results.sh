export DROID_CURATED_ROOT FINO_RAW_ROOT FINO_MANIFEST_PATH FINO_PSEUDO_MANIFEST_PATH FINO_CONVERTED_ROOT DROID_FAILURE_MANIFEST_PATH DROID_FAILURE_PSEUDO_MANIFEST_PATH DROID_FAILURE_CONVERTED_ROOT DROID_FAILURE_BUNDLE
